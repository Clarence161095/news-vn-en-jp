def expand_cache_to_10k(output_file='katakana_cache.json', batch_size=10, delay=1.0):
    """
    Expand existing cache by adding new words
    """
    translator = GoogleTranslator(source='ja', target='en')
    
    # Start with existing cache
    cache = dict(EXISTING_CACHE)
    failed = []
    
    # Filter out words already in cache
    words_to_translate = [w for w in ADDITIONAL_WORDS if w not in cache]
    
    total = len(words_to_translate)
    print(f"Starting translation of {total} new Katakana words...")
    print(f"Existing cache: {len(EXISTING_CACHE)} words")
    print(f"Target total: {len(EXISTING_CACHE) + total} words")
    print(f"Batch size: {batch_size}, Delay: {delay}s")
    print("-" * 60)
    
    for i, katakana in enumerate(words_to_translate, 1):
        try:
            # Translate
            english = translator.translate(katakana)
            
            if english and english.lower() != katakana.lower():
                cache[katakana] = english
                current_total = len(cache)
                print(f"[{i}/{total}] âœ“ {katakana} â†’ {english} (Total: {current_total})")
            else:
                print(f"[{i}/{total}] âœ— {katakana} â†’ FAILED (no translation)")
                failed.append(katakana)
            
            # Save periodically (every 50 words)
            if i % 50 == 0:
                with open(output_file, 'w', encoding='utf-8') as f:
                    json.dump(cache, f, ensure_ascii=False, indent=2)
                print(f"\nğŸ’¾ Progress saved: {len(cache)} words total\n")
            
            # Delay between batches
            if i % batch_size == 0:
                print(f"â¸ï¸  Pausing for {delay}s to avoid rate limiting...\n")
                time.sleep(delay)
                
        except Exception as e:
            print(f"[{i}/{total}] âŒ ERROR: {katakana} â†’ {e}")
            failed.append(katakana)
            
            # Save on error
            with open(output_file, 'w', encoding='utf-8') as f:
                json.dump(cache, f, ensure_ascii=False, indent=2)
    
    # Final save
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(cache, f, ensure_ascii=False, indent=2)
    
    # Summary
    print("\n" + "=" * 60)
    print("CACHE EXPANSION COMPLETE!")
    print("=" * 60)
    print(f"âœ“ Original cache: {len(EXISTING_CACHE)} words")
    print(f"âœ“ New words added: {len(cache) - len(EXISTING_CACHE)} words")
    print(f"âœ“ Total cache size: {len(cache)} words")
    print(f"âœ— Failed: {len(failed)} words")
    print(f"ğŸ“„ Output file: {output_file}")
    print(f"ğŸ“¦ File size: {len(json.dumps(cache, ensure_ascii=False))} bytes")
    
    if failed:
        print(f"\nâš ï¸  Failed words:")
        for word in failed[:20]:
            print(f"  - {word}")
        if len(failed) > 20:
            print(f"  ... and {len(failed) - 20} more")
    
    return cache

