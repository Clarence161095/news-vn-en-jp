def generate_katakana_cache(output_file='katakana_cache.json', batch_size=10, delay=1.0):
    """
    Generate JSON cache file for Katakana â†’ English translations
    
    Args:
        output_file: Output JSON file path
        batch_size: Number of translations per batch (to avoid rate limiting)
        delay: Delay between batches in seconds
    """
    translator = GoogleTranslator(source='ja', target='en')
    cache = {}
    failed = []
    
    total = len(KATAKANA_WORDS)
    print(f"Starting translation of {total} Katakana words...")
    print(f"Batch size: {batch_size}, Delay: {delay}s")
    print("-" * 60)
    
    for i, katakana in enumerate(KATAKANA_WORDS, 1):
        try:
            # Skip if already exists (in case of resuming)
            if katakana in cache:
                print(f"[{i}/{total}] SKIP: {katakana} (already cached)")
                continue
            
            # Translate
            english = translator.translate(katakana)
            
            if english and english.lower() != katakana.lower():
                cache[katakana] = english
                print(f"[{i}/{total}] âœ“ {katakana} â†’ {english}")
            else:
                print(f"[{i}/{total}] âœ— {katakana} â†’ FAILED (no translation)")
                failed.append(katakana)
            
            # Save periodically (every 50 words)
            if i % 50 == 0:
                with open(output_file, 'w', encoding='utf-8') as f:
                    json.dump(cache, f, ensure_ascii=False, indent=2)
                print(f"\nğŸ’¾ Progress saved: {len(cache)} words cached\n")
            
            # Delay between batches to avoid rate limiting
            if i % batch_size == 0:
                print(f"â¸ï¸  Pausing for {delay}s to avoid rate limiting...\n")
                time.sleep(delay)
                
        except Exception as e:
            print(f"[{i}/{total}] âŒ ERROR: {katakana} â†’ {e}")
            failed.append(katakana)
            
            # Save on error (in case we need to resume)
            with open(output_file, 'w', encoding='utf-8') as f:
                json.dump(cache, f, ensure_ascii=False, indent=2)
    
    # Final save
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(cache, f, ensure_ascii=False, indent=2)
    
    # Summary
    print("\n" + "=" * 60)
    print("TRANSLATION COMPLETE!")
    print("=" * 60)
    print(f"âœ“ Successfully translated: {len(cache)} words")
    print(f"âœ— Failed: {len(failed)} words")
    print(f"ğŸ“„ Output file: {output_file}")
    print(f"ğŸ“¦ File size: {len(json.dumps(cache, ensure_ascii=False))} bytes")
    
    if failed:
        print(f"\nâš ï¸  Failed words:")
        for word in failed[:20]:  # Show first 20
            print(f"  - {word}")
        if len(failed) > 20:
            print(f"  ... and {len(failed) - 20} more")
    
    return cache

